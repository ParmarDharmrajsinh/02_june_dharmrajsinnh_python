##Write a Python program to access elements at different index positions. 


# Python program to access elements at different index positions

# Creating a sample list
sample_list = ['apple', 'banana', 'cherry', 'date', 'fig', 'grape']

# Accessing elements using positive indexing
print("Accessing elements using positive indexing:")
print("Element at index 0:", sample_list[0])
print("Element at index 2:", sample_list[2])
print("Element at index 5:", sample_list[5])

# Accessing elements using negative indexing
print("\nAccessing elements using negative indexing:")
print("Element at index -1 (last element):", sample_list[-1])
print("Element at index -3:", sample_list[-3])
print("Element at index -6 (first element):", sample_list[-6])
